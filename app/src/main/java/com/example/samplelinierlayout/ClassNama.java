package com.example.samplelinierlayout;

public class ClassNama {
    private String Nama;

    //inisialisasi objek
    public  ClassNama(String nama)
    {
        this.Nama = nama;
    }

    //membuat method getNama untuk mengembalikan ke variabel Nama
    public String getName()
    {
        return this.Nama;
    }
}